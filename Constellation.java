public class Constellation {
    public int _Spd;
    public int _Agl;
    public int _Dur;
    public int _Atk;
    public int _Grv;
    public int _Lit;
    public int _Edr;
    public int Speed(int Spd) {
        this._Spd = Spd;
        return _Spd;
    }
    public int Agility(int Agl) {
        this._Agl = Agl;
        return _Agl;
    }
    public int Durability(int Dur) {
        this._Dur = Dur;
        return _Dur;
    }
    public int Attack(int Atk) {
        this._Atk = Atk;
        return _Atk;
    }
    public int Gravity(int Grv) {
        this._Grv = Grv;
        return _Grv;
    }
    public int Light(int Lit) {
        this._Lit = Lit;
        return _Lit;
    }
    public int Endurance(int Edr) {
        this._Edr = Edr;
        return _Edr;
    }
}